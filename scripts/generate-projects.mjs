function slugify(name) {
  const base = name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
  return base.startsWith("ai-") ? base : `ai-${base}`;
}

const projects = [];
let id = 1;
for (const [category, names] of Object.entries(CATEGORIES)) {
  for (const name of names) {
    const slug = slugify(name);
    projects.push({
      id,
      name: name.match(/^ai /i) ? name : `AI ${name.charAt(0).toLowerCase()}${name.slice(1)}`.replace(/^AI a/i, "AI A"),
      slug,
      category,
      priority: PRIORITY_SLUGS.includes(slug) ? 1 : 0,
    });
    id++;
  }
}

// Apply priority slug + display name overrides
const usedSlugs = new Set();
for (const override of PRIORITY_OVERRIDES) {
  const matchLower = override.match.toLowerCase();
  const project = projects.find(
    (p) =>
      p.name.toLowerCase().includes(matchLower) ||
      p.name.toLowerCase() === `ai ${matchLower}`
  );
  if (project) {
    project.slug = override.slug;
    project.name = override.name;
    project.priority = 1;
    usedSlugs.add(override.slug);
  }
}

// Dedupe slugs (non-priority projects that collide get suffix)
for (const p of projects) {
  if (usedSlugs.has(p.slug)) continue;
  let slug = p.slug;
  let n = 2;
  while (usedSlugs.has(slug)) {
    slug = `${p.slug}-${n++}`;
  }
  p.slug = slug;
  usedSlugs.add(slug);
}

const output = {
  version: 1,
  githubOwner: "abhia1137",
  daysPerProject: 4,
  commitsPerDay: 3,
  priorityQueue: PRIORITY_SLUGS,
  projects,
};

const outPath = join(__dirname, "..", "data", "projects.json");
writeFileSync(outPath, JSON.stringify(output, null, 2));
console.log(`Wrote ${projects.length} projects to ${outPath}`);
